  :<b>13</b>-14-15-16-17-<b>18</b>) → 5 fois la mise</li>
  <li>Mise sur douze numéros (douzaine ou colonne&#160;: exemple&#160;:
   colonne 1-34 ou douzaine 1-12) → 2 fois la mise</li>
  <li>Mise sur une chance simple, soit dix-huit numéros (Noir-Rouge - Pair-Impair - Manque-Passe)
  → 1 fois la mise</li>
  <li>Mise sur vingt-quatre numéros (deux colonnes adjacentes, vingt-quatre premiers (1-24)
  ou vingt-quatre derniers (13-36), ces mises étant placées à cheval sur les colonnes ou les douzaines)
  → la moitié de la mise
  </li>
  </ul>
  <ul><li>Le zéro fait perdre les mises engagées sur les chances multiples
