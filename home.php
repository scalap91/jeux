  <h2>Gains du casino
  <ul>
  <li>Le casino récupère toutes les mises perdantes (sauf lorsque sort le «&#160;0&#160;» où les mises
   sur les chances simples sont «&#160;en prison&#160;»)</li>
  <li>À la roulette française, il y a 37 cases, or le casino redistribue 35 fois la mise
  (plus la mise elle-même) pour le paiement d'un «&#160;plein&#160;».</li>
  <li>À la roulette française en ligne, sur les paris en numéro plein, le casino se paie non pas
  sur les pertes des joueurs, mais sur les gains de ceux-ci&#160;! Le casino ne paie que 35 fois
  la mise alors
   qu'il y a 37 numéros. Le Zéro est considéré comme une malchance par le joueur,
   alors qu'il n'est que l'artifice qui permet au casino de prendre «&#160;l'avantage de la maison&#160;»
   qui lui revient. En réalité le joueur paie le casino quand il gagne, et quand il perd,
    sa mise va au «&#160;Pot&#160;».</li>
  </ul>
  <h2>Règles de jeux
 Le préalable aux jeux
  <ul>
    <li>
  Avant d’ouvrir la table de jeu, le croupier annonce ostensiblement devant le responsable de
  la salle de jeu et le chef, le compte et le total de l’ensemble des jetons à valeur faciale de la banque
   de la roulette.</li>
  <li>À partir de ce moment, dès qu’un joueur se présente, le croupier annonce ostensiblement que la table
   de jeux de la roulette anglaise est ouverte et que les jeux sont possibles avec l’annonce&#160;:
    «&#160;Faites vos jeux&#160;».
  </li>
</ul>
  <h3>Jeux</h3>
  <ul>
  <li>Dès l’annonce du croupier de «&#160;Faites vos jeux&#160;», l’ensemble des joueurs présents
  autour de la table peuvent effectuer leurs mises.</li>
  <li>Le croupier attrape la bille de la roulette</li>
  <li>Il lance la roulette dans le sens contraire où elle tournait</li>
  <li>Il plaque la bille contre la rigole entourant la roulette et la lance dans le sens contraire
   du sens de rotation de la roulette. La bille doit faire 3 tours du cylindre pour que le jeu soit valable
   </li>
  <li>Avant que la bille ne vienne rencontrer l’un des éléments en laiton perturbant la parcours de
   la bille avant de tomber dans la roulette, le croupier annonce ostensiblement&#160;:
   «&#160;Rien ne va plus&#160;».</li>
  <li>Dès lors, sauf sous la direction du croupier ou de son chef, aucune mise ne peut être placée
   sur le tapis.</li>
  <li>Dès que la bille s’arrête dans l’une des cases, le croupier annonce le numéro gagnant ainsi
  que les gains simples associés.</li>
  <li>Le croupier pose un poids, appelé le <i>dolly</i>, sur le numéro gagnant et effectue les paies
   des mises gagnantes. Les gains «&#160;plein&#160;», «&#160;cheval&#160;», «&#160;transversale&#160;»,
    «&#160;carré&#160;» et «&#160;sizain&#160;» sont annoncés ostensiblement et les jetons sont transmis
     directement aux joueurs. Les autres paies (colonnes, douzaines et chances simples) sont placées
      à côté des jetons joués.</li>
  <li>Les paies effectuées, le croupier retire le <i>dolly</i> du numéro et annonce ostensiblement «&#160;
  Faites vos jeux&#160;». Dès lors, les mises gagnantes placées sur le tapis peuvent être reprises par
   les joueurs.</li></ul>
  <p>Le déroulement ci-dessus est celui codifié pour la roulette anglaise.
  Il est sensiblement le même à la roulette française. Cependant, dans cette dernière version,
   il n'y a pas de <i>dolly</i>, et un des croupiers désigne la numéro gagnant en pointant la case
   sur le tableau à l'aide d'un râteau
  </p>
  <h3>Aléas</h3>
  <dl><dt>La bille tombe dans le cylindre entre le moment où le croupier attrape celle-ci et son
  lancer au fond de la rigole. Le croupier annonce ostensiblement «&#160;rien ne va&#160;».</dt></dl>
  <ul><li>Le croupier prend la bille et la replace dans la case correspondant au numéro précédemment sorti
   sans arrêter la rotation du cylindre</li>
  <li>Le croupier ré-effectue la procédure de mise en jeu&#160;: prise de la bille et changement de
   la rotation du cylindre</li>
  <li>Dès que la bille est mise en rotation, le croupier annonce ostensiblement&#160;:
  «&#160;tout va&#160;»
  </li>
  </ul>
  <dl><dt>Un joueur trop loin d’une case lance son jeton, mais ce dernier n’arrive pas ou n’atteint pas
  la position voulue</dt></dl>
  <ul>
  <li>Le croupier demande au joueur où le joueur annonce sa mise. Le croupier déplace le jeton en
   conséquence. Il annonce ostensiblement qu’il a placé le jeton&#160;: «&#160;… placé&#160;»</li></ul>
  <dl><dt>Le croupier ou son chef voudrait vérifier une mise maximale pendant les mises</dt></dl>
  <ul><li>Le croupier annonce ostensiblement&#160;: «&#160;Je vérifie la mise du …&#160;».
   Il retire la mise et la place devant lui, il effectue le comptage ostensible du nombre de jetons et replace
   la mise. Si des jetons sont en sur-nombre, ils sont redonnés au joueur
   </li>
   </ul>
  <dl><dt>Le croupier vérifie le nombre de jetons pour un paiement</dt></dl>
  <ul><li>Le croupier annonce ostensiblement&#160;: «&#160;Je retire les mises du … pour comptage&#160;».
   Il retire le poids, prend le tas de jetons et les place devant lui. Il effectue ostensiblement le décompte,
    et repose les jetons sur la case de manière que tous les joueurs puissent vérifier a posteriori le compte
    et il repose le poids sur la pile. Il effectue ensuite la mise en paiement.</li></ul>
  <dl><dt>Un joueur voudrait effectuer un change de jeton pendant que la bille tourne</dt></dl>
  <ul><li>Le croupier annonce ostensiblement «&#160;Change après le jeu&#160;». Le paiement fini,
  le croupier procède au change
  </li>
  </ul>
  <dl><dt>Un même numéro sort deux fois de suite</dt></dl>
  <ul><li>Après avoir annoncé le numéro gagnant, le croupier annonce ostensiblement&#160;: «&#160;
  Répétition du numéro&#160;»</li></ul>
  <dl><dt>Le croupier effectue une erreur (erreur dans le paiement, erreur dans les annonces)</dt></dl>
  <ul><li>Celui-ci se met aux ordres de son chef et corrige son erreur.
  </li>
  </ul>
  <h3> Autres règles</h3>
  <ul><li>Le croupier ne peut rien prendre de main à la main. Le joueur doit déposer préalablement son
  jeton sur le tapis avant que le croupier puisse le reprendre.</li>
  <li>Le croupier doit, avant et après le lancer de la bille ou dès qu’il a touché un jeton
  (pour paiement, placement ou change) montrer aux joueurs qu’il n’a plus rien dans les mains.</li>
  <li>À la roulette anglaise, dans certains casinos, il est indiqué que les annonces sont interdites.
   Cela signifie que le joueur ne peut demander au croupier d'effectuer la totalité d'un placement
   obéissant à une annonce (par exemple&#160;: «&#160;le tiers par deux pièces&#160;»,
    «&#160;figure du 9&#160;»). Le croupier peut cependant terminer un placement si et seulement si le joueur
   l'a commencé lui-même, ce dernier énonçant alors des ordres du type  «&#160;complétez le tiers&#160;»,
   ou «&#160;complétez la figure du 9&#160;».
      </li>
      </ul>
