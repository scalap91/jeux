  de leur valeur (la banque en garde la moitié)
  </li>
  <li>
  <i>à la roulette française</i> sont «&#160;emprisonnées&#160;» et le tirage suivant détermine
  si le joueur récupère sa mise (si sa chance simple est tirée), sans paiement, ou si elle est perdue.
