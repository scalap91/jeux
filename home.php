  <h3>
    <ol>
    <li> A Le Black-jack</li>
    </ol>
  </h3>
  <h4>LES REGLES</h4>
  <p>La partie oppose tous les joueurs contre la banque. Le but est de battre le croupier sans dépasser 21.
   Dès qu'un joueur fait plus que 21, on dit qu'il «&#160;saute&#160;» ou qu'il «&#160;crève&#160;»
  et il perd sa mise initiale. La valeur des cartes est établie comme suit&#160;:
    </p>
  <ul>
    <li>de 2 à 9 → valeur nominale de la carte
    </li>
    <li>chaque figure + le 10  surnommées "bûche"  → 10 points
    </li>
    <li>l'As → 1 ou 11 (au choix)
    </li>
  </ul>
    <p>Un Blackjack est composé d'un As et d'une «&#160;buche&#160;»
(carte ayant pour valeur 10, donc 10, J, Q ou K).
Cependant, si le joueur atteint le point 21 en 3 cartes ou plus on compte le point 21 et non pas Blackjack;
 de même lorsque le joueur sépare deux as et qu'il reçoit une buche pour l'un d'eux
    </p><p>Au début de la partie le  distribue une carte face visible à chaque joueur et tire une carte face
     visible également pour lui. Il tire ensuite pour chacun une seconde carte face visible et tire une
      seconde carte face cachée pour lui au Blackjack américain.
    Au blackjack européen, le croupier tire sa seconde carte après le tour de jeu des joueurs.
    </p><p>Puis il demande au premier joueur de la table (joueur situé à sa gauche) l'option qu'il désire
    choisir. Si le joueur veut une carte, il doit l'annoncer en disant «&#160;Carte&#160;!&#160;».
    Le joueur peut demander autant de cartes qu'il le souhaite pour approcher la valeur sans la dépasser.
    Si après le tirage d'une carte, il a dépassé 21, il perd sa mise et le croupier passe au joueur suivant.
    S'il décide de s'arrêter, en disant «&#160;Je reste&#160;», le croupier passe également au joueur suivant.
    </p>
    <p>Le croupier</a> répète cette opération jusqu'à ce que tous les joueurs soient servis.
    </p>
    <p>Ensuite, il joue pour lui selon une règle simple et codifiée «&#160;la banque tire à 16,
     reste à 17&#160;». Ainsi, le croupier tire des cartes jusqu'à atteindre un nombre compris entre 17 et 21
     qu'on appelle un point. S'il fait plus de 21, tous les joueurs restants gagnent mais s'il fait son point,
      seuls gagnent ceux ayant un point supérieur au sien (sans avoir sauté). Dans cette situation,
      le joueur remporte l'équivalent de sa mise. En cas d'égalité le joueur garde sa mise mais n'empoche rien
       en plus. À noter que le blackjack (une bûche et un as en deux cartes) est plus fort que 21
       fait en ayant tiré plus de deux cartes.
       Si un joueur fait blackjack et que le banquier fait 21 en 3 cartes ou plus,
       le joueur fait blackjack et remporte le double de sa mise. Le banquier lui gagne contre tous les joueurs
       ayant 20 ou moins. Réciproquement si la banque a un as et une figure, elle gagne contre tout joueur
        ayant 21 en ayant tiré plus de deux cartes. Dans ce cas, si un joueur fait également blackjack,
        il peut récupérer sa mise mais n'est pas payé, le jeu étant à égalité.
    </p><p>Ces règles simples ne sont contrariées que par quatre cas particuliers&#160;:
    </p>
    <ul><li><b>L'assurance</b>. Si la première carte du croupier est un As, un joueur a la possibilité
    de s'assurer contre son Blackjack. Pour cela, le joueur paye la moitié de sa mise initiale.
     Si le croupier fait Blackjack, le joueur perd sa mise mais se voit payer l'assurance en double
     (donc bénéfice 0, perte 0). Si le croupier ne fait pas Blackjack, deux situations sont possibles&#160;:
      Premièrement, le joueur gagne&#160;; il perd son assurance mais empoche l'équivalent de sa mise initiale
       (bénéfice net 1/2 fois la mise initiale). Deuxièmement, le joueur perd&#160;; il perd alors l'assurance
        ainsi que sa mise (perte 1,5 fois la mise initiale). Il peut bien sûr Tirer/Doubler/Séparer après avoir
         pris l'assurance.</li></ul>
    <ul>
      <li>
        <b>Le split</b>. Lorsqu'un joueur obtient deux cartes de même valeur, il lui est possible de séparer
     ces deux cartes afin de jouer avec deux jeux. Pour cela, il doit ajouter la même mise que celle qu'il
      a jouée au départ. Une fois les jeux séparés, le joueur joue chaque main de la même façon qu'une main
       simple. S'il gagne sur les deux jeux, il aura fait d'une pierre deux coups (deux gains en un coup).
        Si le joueur perd sur un jeu, il ne gagne pas mais ne perd rien. Enfin, s'il perd sur les deux jeux,
         il aura perdu deux fois en un coup. Si à la suite d'une séparation,
         le joueur obtient une troisième carte de même valeur (sur le premier ou deuxième tableau) il peut
         à nouveau les séparer et ainsi jouer sur 3 tableaux.
         En France, cette règle est cependant au bon vouloir des casinos
         (certains autorisent deux jeux maximum, d'autres trois jeux maximum).</li></ul>
    <p>Une règle particulière concerne la séparation de deux as dans la majorité des casinos,
    en séparant deux as, le joueur ne peut plus recevoir qu'une carte pour chacun des tableaux et ne
    pourra pas spliter à nouveau s'il reçoit un troisième as.
    </p>
    <p>Précision importante. Le blackjack n'est pas possible en ayant splité&#160;:
     un joueur qui split deux as et reçoit deux bûches aura deux 21, mais pas deux blackjack,
     celui-ci devant être réalisé dès les deux premières cartes.
     Il s'ensuit pour l'exemple que le joueur ayant séparé deux as et recevant deux bûches perd tout
     contre un as et une figure à la banque.
    </p>
    <ul><li><b>Doubler</b>. Après avoir reçu deux cartes, le joueur peut choisir de doubler sa mise à la seule
    condition de ne recevoir qu'une carte après cela. Exemple après la donne,
    j'ai 11 (8 et 3). Le croupier a une carte faible, 6 par exemple.
    Je choisis alors de doubler ma mise.
    L'espoir est de recevoir une bûche pour obtenir le point 21 alors que le croupier a peu de chances de faire
     de même. Il est tout à fait possible de doubler après avoir séparé.</li></ul>
    <p>En France, jusqu'en 2007, il n'était possible de doubler que si le score des deux premières cartes
     était 9, 10 ou 11. Cette règle a été abolie
     et il est désormais possible de doubler avec n'importe quel score au blackjack joué dans les casinos
      français.
    </p>
    <p>Au Blackjack américain, le joueur double face cachée (<i>doubling down</i>),
    c'est-à-dire que la carte qu'il reçoit après avoir doublé lui est distribuée face cachée.
    Il ne la voit qu'une fois le jeu du croupier terminé.
    </p>
  <ul>
    <li>
    <b>Abandonner</b>. Une règle qui n'est pas universellement admise, mais néanmoins prévue par
     la législation française et appliquée par certains casinos. Après avoir reçu ses deux premières cartes
      et constatant une carte dangereuse à la banque, un joueur peut décider d'abandonner immédiatement.
       La banque récupère alors la moitié de sa mise.
   </li>
  </ul>
    <p>À sa création, le Blackjack se jouait avec un seul jeu de 52 cartes. Cependant,
    ce jeu unique représentait une faille vis-à-vis de l'avantage du casino. En effet,
    </p>
    <ul>
      <li>L’<b>encaisse</b>. Elle contient les jetons destinés au paiement des joueurs.
    Positionnée juste devant le croupier, elle est minutieusement fermée, puis comptée par le croupier
     sous le regard du chef de salle.</li>
    <li>Le <b>sabot</b>. Traditionnellement une boîte en plastique (souvent noire) dans laquelle
    est introduit l'ensemble des cartes, permettant au croupier une distribution rapide et fluide.
     Elle est aujourd'hui remplacée dans la plupart des casinos par des CSM (voir plus bas).</li>
    <li>Le <b><i>rack</i> de défausse</b>. Ce <i>rack</i> est conçu pour accueillir les cartes jouées
    au fil du jeu.</li>
    <li>La <b>fente à pourboires</b>.</li>
    <li>Enfin, la table de Blackjack est marquée de 7 cases appelées <i><b>spots</b></i>,
    dans lesquelles les joueurs placent leurs mises. Le croupier distribue les cartes devant les cases
     respectives à chaque joueur.
    </li>
  </ul>
    <p>Les Continuous Shuffling Machines (CSM), qu'on pourrait traduire en français par <b>mélangeur de cartes
    en continu</b> remplaçant le sabot classique et mélangeant les cartes tout au long de la partie.
    Ces machines sont installées par les casinos pour augmenter les revenus de leurs tables en accélérant
    le jeu (le croupier n'a plus à mélanger les cartes quand le sabot est vide,
    augmentant ainsi mathématiquement les mises des joueurs au cours d'une partie de même durée) et en empêchant leurs tables d'être vulnérables au comptage des cartes.
    </p>
    </p>
    <ul>
      <li>La dégradation de l'ambiance de table, qui par l'absence de temps mort dus aux mélanges, s'apparente de plus en plus à une machine à sous plutôt qu'à un espace convivial.</li>
    <li>La dévalorisation du rôle du croupier qui ne se charge plus du mélange.</li>
    <li>L'utilisation d'un lecteur optique, sorte de scanner qui analyse la carte pour la reconnaître,
     entraînant de la part de certains joueurs des suspicions d'un mélange défavorable au joueur.</li></ul>
    <h2>
     Stratégies
   </h2>
  <h3>
    Stratégie de base
  </h3>
    <p>Le Blackjack, contrairement à d'autres jeux, dépend également du choix du joueur,
    qui peut réduire l'avantage du casino, en jouant avec la stratégie de base.
     Cette stratégie détermine quand tirer une carte, et quand s'arrêter.
     Elle détermine également lorsqu'il est préférable de doubler la mise ou de séparer deux cartes égales.
    </p><p>Cette stratégie se base sur une comparaison entre les cartes du joueur et la carte visible
    du croupier. Il existe toutefois des variations dans cette stratégie de base,
    dépendant des règles utilisées par la maison et du nombre de jeux de cartes utilisés.
     Elle est néanmoins appliquée avec les règles communes dans la plupart des casinos américains
     contenant 6 à 8 jeux de cartes, comme à Las Vegas.
    </p>
