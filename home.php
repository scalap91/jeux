      <ol>
        <li> B La Roulette Anglaise</li>
    </ol>
  </h3>
  <ul><li>37 cases numérotées de 0 à 36 (faisant corps avec le tambour) alternativement rouges et noires,
   à l'exception du zéro, vert.</li></ul>
  <p>Pour la roulette américaine&#160;:
  </p>
  <ul>
  <li>38 cases numérotées de 0 à 36 (faisant corps avec le tambour) alternativement rouges et noires,
