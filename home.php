  <p>Le cylindre alterne systématiquement cases rouges et noires.
  Il minimise également au maximum le voisinage de deux nombres pair/impair et de deux manque/passe.
  </p>
  <h2>La bille</h2>
  <ul>
  <li>Une bille en ivoire (aujourd'hui en résine ou en Téflon) que le croupier lance en sens inverse
   de la rotation de la roulette (le lancer de la boule, c'est ainsi qu'on l'appelle, alterne de même
   que la rotation du plateau après chaque numéro sorti) et qui va s'arrêter sur un numéro de la roulette.
   </li>
   </ul>
  <h2>
  </div>La table de jeu</div>
</h2>
</div>
</div>
  <p>Sur la table de jeu des cases correspondant aux couleurs et aux numéros de la roulette sont dessinées
  (ici illustration du tableau de roulette française aussi appelée européenne).
   Il existe des différences entre les différentes roulettes&#160;:
  </p>
  <ul>
  <li>les numéros ne sont pas colorisés sur une table de roulette française
  </li>
  <li>Il y a un espace vide à la roulette française pour chacune des chances simples,
  on y met les mises emprisonnées lorsque le 0 sort
  </li>
  <li>Sur certaines roulettes dans certains casinos, notamment en Belgique,
  il est possible de miser sur un quart (par exemple le quart 1-9), une zone de mise est prévue entre
   les douzaines et les numéros
   </li>
  <li>La table de roulette française est bien plus large et bien plus basse qu'une roulette anglaise&#160;;
   on joue assis à une table de roulette française et les croupiers travaillent aussi assis
   </li>
   </ul>
  <div>Le croupier</div>
  <ul>
  <li>Le croupier est un employé du casino qui est chargé de diriger le jeu,
  de manipuler la bille et le cylindre, d'enregistrer les mises, de régler les gains des joueurs et d'annoncer
  les phases de jeu par les phrases suivantes&#160;:
  <ul>
  <li>«&#160;Faites vos jeux&#160;» (début des mises, après paiement des gains de la précédente)
  </li>
  <li>«&#160;Les jeux sont faits&#160;» (lancement de la bille)
  </li>
  <li>«&#160;Rien ne va plus&#160;» (fin des mises, la sortie du numéro gagnant étant imminente)
  </li>
  <li>«&#160;14 rouge pair et manque&#160;» (exemple de sortie de numéro),
  suivi des mises gagnantes des joueurs dans l'ordre décroissant des gains (exemple&#160;: «&#160;Sixain,
  Transversale, Cheval, Plein&#160;») ou de «&#160;Rien au numéro&#160;» si aucun joueur ne gagne sur ce coup.
  </li>
  <li>«&#160;Rien ne va&#160;» (si la bille est sortie accidentellement du cylindre avant de s'immobiliser sur
   un numéro)</li>
   </ul>
   </li>
   </ul>
  <p>La roulette anglaise et américaine emploie traditionnellement un seul croupier,
  la roulette française emploie quant à elle le plus souvent 4 croupiers,
  deux croupiers de chaque côté de la roulette et un en bout de table, souvent appelé sous Chef.
   Le tout surveillé par un Chef de Table se situant à l’opposé du sous Chef.
  </p>
  <h2>Les plaques</h2>
  <ul>
  <li>Les plaques sont des jetons correspondant à une somme d'argent,
  que les joueurs achètent à la caisse du casino.
  </li>
  <li>Ils les déposent sur les cases  de la table de jeu pour miser sur un numéro.
  </li>
  <li>S'ils gagnent, le croupier leur distribue leurs gains sous forme de plaques et jetons
  </li>
  </ul>
  <p>À la roulette anglaise et américaine, on peut jouer avec des plaques uniquement sur les chances simples
  &#160;; pour les tiers (douzaines et colonnes), les sizains, les carrés, les transversales,
   les chevaux et les pleins, on utilise exclusivement des jetons de couleurs,
   et chaque joueur joue avec sa propre couleur. À la roulette française,
   on peut jouer avec des plaques sur l'intégralité de la table.
  </p>
  <h2>Gains des joueurs
  <ul>
  <li>Mise sur un numéro (plein) → 35 fois la mise</li>
  <li>Mise sur deux numéros (cheval&#160;: exemple&#160;: 7-10) → 17 fois la mise</li>
  <li>Mise sur trois numéros (transversale dite aussi «&#160;transversale pleine&#160;»&#160;: exemple&#160;:
     7-8-9) → 11 fois la mise</li>
  <li>Mise sur quatre numéros (carré&#160;: exemple&#160;:<b>25</b>-26-28-<b>29</b>) → 8 fois la mise.
  </li>
