  <li>le texas hold'em poker </li>
</ol>
</h3>
<h4>LES REGLES</h4>
<li>
  <p>&nbsp;</p>
  <ul>
  <li>Le jeu du texas hold’em poker se joue avec un jeu de cinquante-deux cartes.
  Le croupier mélange ostensiblement les cartes devant les joueurs. Le croupier affecté à la table anime
  la partie et contrôle son bon déroulement, invite les joueurs à miser. Il assure également la distribution
  des cartes.</li>
  <li>Une salle est consacrée uniquement au texas hold’hem poker et son l’accès est soumis à l’enregistrement
   de l’identité des joueurs, les casinos exploitant ce jeu doivent agencer un espace qui lui est
   spécifiquement dédié.</li>
  <li>La partie débute en présence de deux joueurs minimum. Ceux-ci, installés à la table, assistent à la
   comptée et à la vérification des cartes. Aucun joueur debout ne peut participer au jeu. La partie se
   poursuit tant que deux joueurs participent encore au jeu.</li>
  <li>Le nombre de joueurs assis est au maximum de dix. Avant le début de la séance ou lorsque toutes les
   places aux tables de jeu sont attribuées, les joueurs ont la possibilité de se faire inscrire sur une
   liste d’attente qui est placée sous la responsabilité du chef de partie.</li>
  <li>Il ne peut être procédé à aucune opération de change à table. Les mises ne peuvent être représentées
  que par des jetons ou des plaques. Aucun enjeu sur parole n’est toléré. Les joueurs doivent se présenter
   à la table de jeu avec une mise initiale dite cave constituée de plaques et jetons dont le change est
   effectué en caisse. Le montant minimum de la cave initiale est déterminé par la direction de
   l’établissement, à chaque début de séance.</li>
  <li>Au cours d’une partie, le joueur a la possibilité de se recaver. Aucune recave n’est acceptée pendant
   un coup. Le montant maximum de la recave est fixé par le règlement intérieur. Avant chaque donne, les deux
    joueurs placés immédiatement à gauche du bouton sont tenus de déposer, devant leur emplacement marqué sur
     le tapis, une mise initiale obligatoire.</li>
  <li>Le premier joueur dépose une mise dite small blind égale à la moitié de la mise minimum de la table.
   Le second joueur dépose une mise dite big blind dont le montant est au minimum égal à celui de la small
   blind et au maximum le double. Le troisième joueur situé à gauche du bouton peut alors déposer une option
   dont le montant est le double de celui de la big blind.</li>
  <li>Après avoir vérifié que toutes les mises initiales sont conformes, le croupier distribue les cartes,
   une par une, figures cachées, en commençant par le joueur assis à la gauche du bouton et en allant dans
   le sens des aiguilles d’une montre. Chaque joueur reçoit deux cartes qu’il est tenu de protéger.</li>
  <li>Les cartes ne doivent en aucun cas être tenues sous le niveau de la table. Elles doivent demeurer de
   façon visible sur celle-ci.</li>
  <li>A l’issue de la donne, les joueurs prennent connaissance de leur main. Le croupier procède alors au
   premier tour d’enchères. Le joueur situé à gauche du joueur ayant misé la big blind prend la parole en
   premier :&nbsp;</li>
  </ul>
  <ol>
 <li>– il peut passer ;</li>
 <li>– il peut suivre ;</li>
 <li>– il peut relancer.</li>
  </ol>
  <ul>
  <li>Les joueurs suivants, jusqu’au joueur ayant misé la small blind, se déterminent de la même façon.
  &nbsp;Le joueur ayant misé la small blind prend alors la parole :</li>
  </ul>
  <ol>
  <li>– il peut check;<ul>
    (demander à voir le prochain tour de cartes gratuitement)</ul></li>
  <li>– il peut miser.</li>
  </ol>
  <ul>
  <li>Le joueur ayant misé la big blind prend ensuite la parole :&nbsp;</li>
  </ul>
  <ol>
  <li>– il peut passer ;&nbsp;</li>
  <li>– il peut suivre ;&nbsp;</li>
  <li>– il peut relancer.</li>
  </ol>
  <ul>
  <li>Dans le cas où aucun joueur n’a relancé au cours du premier tour d’enchères,
  le joueur ayant misé la big blind peut annoncer parole ou check.
  Cette annonce met fin au premier tour d’enchères.</li>
  <li>En cas de relance du joueur ayant misé la big blind, le croupier procède à un nouveau tour d’enchères
  qui ne se termine qu’au moment où les joueurs encore en jeu ont tous suivi ou relancé.
  Quand le premier tour d’enchères est terminé, le croupier ramasse les mises engagées par tous les joueurs
  en commençant par le premier joueur situé à sa gauche, puis en allant dans le sens des aiguilles
  d’une montre. Ces enjeux ramassés sont placés au centre la table et constituent le pot.</li>
  <li>A l’issue de ce premier tour, le croupier brûle la première carte du paquet et expose trois cartes,
   figures en dessus, qui déterminent le flop.
   Ces trois cartes sont communes à tous les joueurs encore en jeu.
   Le croupier procède à un deuxième tour d’enchères pour les joueurs encore en jeu.
   Le premier joueur encore en jeu à prendre la parole se situe immédiatement à gauche du bouton.
   Le déroulement du deuxième tour est identique au premier.</li>
  <li>Le croupier procède à un troisième tour d’enchères pour les joueurs encore en jeu.
  Tous les joueurs se déterminent exactement de la même façon qu’au second tour d’enchères.
   Quand le troisième tour d’enchères est terminé,
   le croupier ramasse les mises engagées toujours dans le même sens.
   Les enjeux ramassés sont ajoutés au pot.</li>
  <li>A l’issue de ce troisième tour, le croupier brûle la première carte du paquet et expose
  une carte supplémentaire, figure en dessus, qui détermine le river.
  Cette carte supplémentaire est commune à tous les joueurs encore en jeu en plus des trois cartes du flop
  et de la carte du turn. L’ensemble de ces cinq cartes constitue le board.</li>
  <li>Le croupier procède au dernier tour d’enchères pour les joueurs encore en jeu.
  Tous les joueurs se déterminent exactement de la même façon qu’aux deux tours d’enchères précédents.</li>
  <li>Quand le dernier tour d’enchères est terminé,
  le croupier ramasse les mises engagées toujours dans le même sens.
  Les enjeux ramassés sont ajoutés au pot.</li>
  <li>Durant un tour d’enchères, un joueur a la possibilité d’engager la totalité de son tapis
   en enjeu sans dépassement des maxima de relance établis selon la version de hold’em pratiquée.
   Si tous les autres joueurs passent, le joueur ayant engagé son tapis remporte la totalité du pot.</li>
  <li>Dans le cas où le montant total du tapis engagé par le joueur est inférieur aux enjeux engagés
   par les autres joueurs, le croupier procède à la création de deux pots distincts :&nbsp;</li>
  <li>– un pot principal constitué du tapis engagé par le joueur et d’une fraction équivalente
  de l’enjeu de chaque joueur qui a suivi ou relancé jusqu’alors.
  Si la main la plus forte est celle du joueur ayant engagé son tapis,
  le croupier lui remettra le seul pot principal ;&nbsp;</li>
  <li>– un pot extérieur constitué de tous les autres enjeux restants et engagés jusqu’à la fin du coup
   par les autres joueurs. Si la main la plus forte est celle du joueur ayant engagé son tapis,
   le croupier remettra le seul pot extérieur à la deuxième main la plus forte du coup.</li>
  <li>Dans tous les cas, si la main la plus forte est celle d’un autre joueur que celui ayant engagé son tapis
  croupier lui remettra tous les pots.</li>
  <li>Le croupier est en charge de la constitution et de l’agencement des différents pots
  au centre de la table.
  Il peut être constitué autant de pots extérieurs que la partie le nécessite du moment que chaque pot
  est clairement identifiable.</li>
  <li>Le croupier demande au premier joueur à avoir relancé lors du dernier tour d’enchères de procéder
   à l’abattage de ses deux cartes puis à tous les autres joueurs lui succédant
   en allant dans le sens des aiguilles d’une montre.
   Dans le cas où aucun joueur n’a relancé durant le dernier tour d’enchères,
   le premier joueur à abattre ses cartes est celui ayant parlé en premier lors du dernier tour
   d’enchères.</li>
  <li>Dans tous les cas, la meilleure main est déterminée par une combinaison de cinq cartes à partir
  des sept cartes à disposition des joueurs. La main gagnante est la main du joueur dont la combinaison
   est la plus élevée.</li>
  <li>La plus haute des cartes qui composent la combinaison départage les combinaisons équivalentes.</li>
  <li>En cas d’égalité entre les combinaisons de deux joueurs ou plus, faisant appel à moins de cinq cartes,
   la main la plus forte sera celle comportant la carte du joueur la plus élevée en dehors de ces combinaisons.
   </li>
  <li>En cas d’égalité parfaite, le pot sera partagé entre toutes les mains ex aequo.
  Il y a égalité parfaite lorsque les joueurs ont la même combinaison composée de cinq cartes issues
  de leurs deux cartes ou de l’une de leurs deux cartes et du board, ou seulement du board.</li>
  <li>Le croupier soumet le pot au prélèvement opéré au profit de l’établissement.
  Le montant du prélèvement est immédiatement versé dans la cagnotte encastrée dans la table de jeu (4%).
  </li>
  <li>A l’issue de l’opération de prélèvement, le croupier remet au gagnant la totalité du pot minorée
   du prélèvement.</li>
  <li>Pour arrêter la partie, les trois dernières mains devront être annoncées préalablement.</li>
  </ul>
